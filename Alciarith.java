public class Alciarith {
    public int add(int i,int j){
        int a=i+j;
        return a;
    }
    public int sub(int c,int d){
        int s=c-d;
        return s;
    }
    public int multi(int h,int f){
        int m=h*f;
        return m;
    }
    public int divi(int e,int n){
        int d=e/n;
        return d;
    }
    public int modu(int q,int p){
        int m=q%p;
        return m;
    }
}
